/* Seed the di_questions + di_images collections from the extracted JSON files.
 * Source: Nishit Sinha LRDI for the CAT (Pearson, 6e), Part 2 Section 2
 * "Practising Data Interpretation" — Ch 20 Foundation (8 exercises),
 * Ch 21 Moderate (11), Ch 22 Advanced (4). Each practice exercise => one DI test.
 *
 * Chart/table crops are NOT committed to the repo (public/di/ is gitignored). The JSON
 * keeps a lightweight path (e.g. /di/fnd/1-s1.png); at seed time we read that PNG, store
 * it as base64 in the di_images collection (one doc per data-set), and rewrite the
 * question's context_passage to a short served URL: ![data set](/api/di/image/<set_id>).
 * The image bytes live in MongoDB and are served by GET /api/di/image/:setId.
 * Generated by .varc_extract/extract_di.py.  Run:  node api/seed_di.js
 */
const mongoose = require('mongoose');
const fs = require('fs');
const path = require('path');
require('dotenv').config({ path: path.join(__dirname, '..', '.env.local') });
require('dotenv').config({ path: path.join(__dirname, '..', '.env') });

const QDIR = path.join(__dirname, '..', 'chapter_questions');
const PUBLIC = path.join(__dirname, '..', 'public');
const SOURCE = 'Nishit Sinha LRDI (Pearson, 6e)';

(async () => {
  await mongoose.connect(process.env.MONGODB_STRING, { bufferCommands: false });
  const col = mongoose.connection.db.collection('di_questions');
  const images = mongoose.connection.db.collection('di_images');
  const sessions = mongoose.connection.db.collection('di_sessions');

  const files = fs
    .readdirSync(QDIR)
    .filter(f => /^di_(foundation|moderate|advanced)_\d+\.json$/.test(f))
    .sort();

  const del = await col.deleteMany({ source: SOURCE });
  const delImg = await images.deleteMany({});
  console.log(`Deleted ${del.deletedCount} DI questions, ${delImg.deletedCount} DI images`);

  // Read every referenced PNG once per set_id → base64 di_images docs; rewrite the
  // question's context_passage path to the served-image URL.
  const imageDocs = {};   // set_id -> { set_id, b64, content_type }
  let missing = 0;
  function rewrite(setId, cp) {
    if (!cp) return cp;
    return cp.replace(/!\[([^\]]*)\]\((\/di\/[^)\s]+\.png)\)/g, (m, alt, url) => {
      const file = path.join(PUBLIC, url);
      if (!fs.existsSync(file)) { missing++; console.warn('  ! missing image:', url); return m; }
      if (setId && !imageDocs[setId]) {
        imageDocs[setId] = { set_id: setId, content_type: 'image/png', b64: fs.readFileSync(file).toString('base64') };
      }
      return `![${alt}](/api/di/image/${setId})`;
    });
  }

  let total = 0;
  const byTier = {};
  for (const file of files) {
    const docs = JSON.parse(fs.readFileSync(path.join(QDIR, file), 'utf8'));
    if (!docs.length) continue;
    const now = new Date();
    const ins = await col.insertMany(
      docs.map(d => ({
        ...d,
        context_passage: rewrite(d.set_id, d.context_passage),
        source: d.source || SOURCE,
        createdAt: now, updatedAt: now,
      })),
      { ordered: false }
    );
    total += ins.insertedCount;
    byTier[docs[0].tier] = (byTier[docs[0].tier] || 0) + ins.insertedCount;
    console.log(`${file}: inserted ${ins.insertedCount} (test ${docs[0].test}, ${docs[0].tier})`);
  }

  const imgList = Object.values(imageDocs);
  if (imgList.length) {
    const now = new Date();
    await images.insertMany(imgList.map(i => ({ ...i, createdAt: now, updatedAt: now })), { ordered: false });
  }

  // Sessions reference question _ids that change on reseed — clear them.
  const cleared = await sessions.deleteMany({});
  console.log(`Cleared ${cleared.deletedCount} stale DI sessions`);

  console.log(`\ndi_questions total: ${total}`, byTier);
  console.log(`di_images stored: ${imgList.length}` + (missing ? ` (${missing} PNGs missing)` : ''));
  await mongoose.disconnect();
  process.exit(0);
})().catch(e => { console.error(e); process.exit(1); });
