/* Seed the di_questions collection from the extracted JSON files.
 * Source: Nishit Sinha LRDI for the CAT (Pearson, 6e), Part 2 Section 2
 * "Practising Data Interpretation" — Ch 20 Foundation (8), Ch 21 Moderate (11),
 * Ch 22 Advanced (4). Each practice exercise => one DI test.
 *
 * IMAGES ARE STORED IN THE DB AS base64 data-URIs (not committed to the repo).
 * The extracted JSON keeps a light image reference, e.g. ![data set 1](/di/fnd/1-s1.png).
 * At seed time we read that PNG from public/di/ (a LOCAL, gitignored working dir — the crops
 * are regenerable from the PDF via .varc_extract/extract_di.py) and inline it as
 * data:image/png;base64,... directly inside context_passage. MathText then renders it as an
 * <img> with no network request — so charts render on prod regardless of static file serving,
 * auth, or 404s. Because nothing in the build runs this seed (build = `next build` only), the
 * DB only changes when this script is run manually; once seeded with data-URIs it stays put.
 * Text caselets pass through unchanged.
 * Generated by .varc_extract/extract_di.py.  Run:  node api/seed_di.js
 */
const mongoose = require('mongoose');
const fs = require('fs');
const path = require('path');
require('dotenv').config({ path: path.join(__dirname, '..', '.env.local') });
require('dotenv').config({ path: path.join(__dirname, '..', '.env') });

const QDIR = path.join(__dirname, '..', 'chapter_questions');
const PUBLIC = path.join(__dirname, '..', 'public');
const SOURCE = 'Nishit Sinha LRDI (Pearson, 6e)';

// Inline every /di/<tier>/<file>.png reference in a passage as a base64 data-URI.
// Cache per path so each file is read+encoded once (a set image repeats across its questions).
const imgCache = new Map();
let missing = 0;
function embedImages(passage) {
  if (!passage || passage.indexOf('/di/') === -1) return passage;
  return passage.replace(/\((\/di\/[a-z]+\/[^)\s]+\.png)\)/g, (m, rel) => {
    let uri = imgCache.get(rel);
    if (uri === undefined) {
      const fp = path.join(PUBLIC, rel);
      if (fs.existsSync(fp)) {
        uri = `data:image/png;base64,${fs.readFileSync(fp).toString('base64')}`;
      } else {
        missing++;
        console.warn(`  WARN missing image on disk: public${rel} (keeping path)`);
        uri = rel; // fallback: leave the path so it's at least recoverable
      }
      imgCache.set(rel, uri);
    }
    return `(${uri})`;
  });
}

(async () => {
  await mongoose.connect(process.env.MONGODB_STRING, { bufferCommands: false });
  console.log(`Connected to ${mongoose.connection.host}/${mongoose.connection.name}`);
  const col = mongoose.connection.db.collection('di_questions');
  const sessions = mongoose.connection.db.collection('di_sessions');
  await mongoose.connection.db.collection('di_images').deleteMany({}).catch(() => {});
  const del = await col.deleteMany({});
  console.log(`Deleted ${del.deletedCount} existing DI questions`);

  const files = fs.readdirSync(QDIR)
    .filter(f => /^di_(foundation|moderate|advanced)_\d+\.json$/.test(f)).sort();

  let total = 0, embedded = 0;
  const byTier = {};
  for (const file of files) {
    const docs = JSON.parse(fs.readFileSync(path.join(QDIR, file), 'utf8'));
    if (!docs.length) continue;
    const now = new Date();
    const out = docs.map(d => {
      const cp = embedImages(d.context_passage);
      if (cp && cp.indexOf('data:image') !== -1) embedded++;
      return { ...d, context_passage: cp, source: d.source || SOURCE, createdAt: now, updatedAt: now };
    });
    await col.insertMany(out, { ordered: false });
    total += out.length;
    byTier[docs[0].tier] = (byTier[docs[0].tier] || 0) + out.length;
    console.log(`${file}: inserted ${out.length} (test ${docs[0].test}, ${docs[0].tier})`);
  }

  const cleared = await sessions.deleteMany({});
  console.log(`Cleared ${cleared.deletedCount} stale DI sessions`);
  console.log(`\ndi_questions total: ${total}`, byTier);
  console.log(`questions carrying an inlined image: ${embedded}; distinct images embedded: ${[...imgCache.values()].filter(v => v.startsWith('data:')).length}; missing: ${missing}`);
  await mongoose.disconnect();
  process.exit(0);
})().catch(e => { console.error(e); process.exit(1); });
